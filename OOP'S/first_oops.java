// Class and Object in Java


public class first_oops {


    void Arun(){

        System.out.println("I am Arun");
    }


    void Jeevesh(){

        System.out.println("I am Jeevesh");
    }




    public static void main(String[] args) {

        first_oops obj = new first_oops();
        obj.Arun(); // calling method of first_oops class
        obj.Jeevesh(); // calling method of first_oops class





    
        
    }
}